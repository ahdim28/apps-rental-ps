    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // cek jenis
        if (jenisSelected == 0) {
            JOptionPane.showMessageDialog(null, "Jenis harus dipilih", "Perhatian", JOptionPane.WARNING_MESSAGE);
        }
        
        // cek jika nama kosong
        if (fieldNama.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nama tidak boleh kosong", "Perhatian", JOptionPane.WARNING_MESSAGE);
        }
        
        // cek jika telepon kosong
        if (fieldNoTelepon.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Telepon tidak boleh kosong", "Perhatian", JOptionPane.WARNING_MESSAGE);
        }
        
        // cek jika alamat
        if (fieldAlamat.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Alamat tidak boleh kosong", "Perhatian", JOptionPane.WARNING_MESSAGE);
        }
        
        // cek jika jam bukan angka
        if (isNotNumber(fieldJam.getText())) {
            JOptionPane.showMessageDialog(null, "Jam harus berupa angka", "Perhatian", JOptionPane.WARNING_MESSAGE);
        }
        
        // cek jika jam kurang dari 1
        if (jam < 1) {
            JOptionPane.showMessageDialog(null, "Jam tidak boleh kurang dari 1", "Perhatian", JOptionPane.WARNING_MESSAGE);
        }
        
        try {
            
            Connection conn = dbConnection.openConnection();
            PreparedStatement preparedStatement = conn.prepareStatement("INSERT INTO penyewa "
                    + "(kode, jenis, nama, kartu_identitas, telpon, alamat, jam, tanggal, total, keterangan, status, tanggal_pengembalian) "
                    + "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, 0, ?)");
            
            preparedStatement.setString(1, fieldKode.getText());
            preparedStatement.setInt(2, jenisSelected);
            preparedStatement.setString(3, fieldNama.getText());
            preparedStatement.setString(4, fieldKartuIdentitas.getText());
            preparedStatement.setString(5, fieldNoTelepon.getText());
            preparedStatement.setString(6, fieldAlamat.getText());
            preparedStatement.setInt(7, jam);
            preparedStatement.setString(8, fieldTanggal.getText());
            preparedStatement.setDouble(9, hargaTotal);
            preparedStatement.setString(10, fieldKeterangan.getText());
            preparedStatement.setString(11, null);
            
            int rowsAffected = preparedStatement.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan", "Info", JOptionPane.INFORMATION_MESSAGE);
                new List().show();
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Data gagal ditambahkan", "Error", JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Form.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed


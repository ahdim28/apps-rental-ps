    // set harga
    private void setHarga() {
        jam = Integer.parseInt(fieldJam.getText());
        
        total = switch (jenisSelected) {
            case 1 -> 3000;
            case 2 -> 5000;
            case 3 -> 8000;
            case 4 -> 10000;
            default -> 0;
        };
        
        if (jam >= 5) {
            total = total / 2;
        }
        
        hargaTotal = total * jam;

        lblTotal.setText("TOTAL : " + String.format("Rp.%,.0f", hargaTotal));
    }
    
    // cek jika bukan nomot
    private static boolean isNotNumber(String str) {
        try {
            
            Integer.parseInt(str);

            // If parsing succeeds, it's a number
            return false;
        } catch (NumberFormatException e) {
            // If an exception is caught, it's not a number
            return true;
        }
    }
    

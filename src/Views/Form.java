    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // get date
        LocalDateTime currentDateTime = LocalDateTime.now();
        
        // set value field kode
        String kodeRental = "RENT-";
        kodeRental += currentDateTime.format(DateTimeFormatter.ofPattern("yyyyMMddHHmmss-"));
        
        try {
            
            Connection conn = dbConnection.openConnection();
            Statement statement = conn.createStatement();
            ResultSet resultSet = statement.executeQuery("SELECT COUNT(*) AS recordCount FROM penyewa");
            if (resultSet.next()) {
                kodeRental += resultSet.getInt("recordCount") + 1;
            }
            
            dbConnection.closeConnection();
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Form.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        fieldKode.setText(kodeRental);
        
        // set value field tanggal
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
        String formattedDateTime = currentDateTime.format(formatter);
        fieldTanggal.setText(formattedDateTime);
    }//GEN-LAST:event_formComponentShown

